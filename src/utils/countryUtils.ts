// Function to get the M3U URL for a specific country
export function getCountryM3UUrl(countryCode: string): string {
  return `${BASE_URL}${countryCode}.m3u`;
}

// Function to get a country by its code
export function getCountryByCode(countryCode: string): Country | undefined {
  return countries.find(country => country.code === countryCode);
}

// Function to get all countries sorted by name
export function getAllCountriesSorted(): Country[] {
  return [...countries].sort((a, b) => a.name.localeCompare(b.name));
}

